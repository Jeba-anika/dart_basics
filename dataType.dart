void main() {
  //Numbers
  int a = 10;
  double d = 10.9;
  print(a);
  print(d);

  //Strings
  String name = 'Jeba';
  String name2 = 'Anika';
  print(name + ' ' + name2);

  //booleans
  bool b = false;
  print(b);

  //Lists
  List myList = ['A', 'B', 'C', 'D'];
  print(myList);

  //Maps
  Map myMap = {
    'name': 'Jeba',
    'age': 24,
  };

  print(myMap);

  //sets
  Set mySet = {1, 2, 3, 4};
  print(mySet);
}
